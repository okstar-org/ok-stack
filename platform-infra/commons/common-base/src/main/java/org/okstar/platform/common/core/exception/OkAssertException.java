package org.okstar.platform.common.core.exception;

/**
 * 基础异常
 * 
 * 
 */
public class OkAssertException extends RuntimeException
{
    private static final long serialVersionUID = 1L;

    /**
     * 所属模块
     */
    private final String module;

    /**
     * 错误码
     */
    private final String code;

    /**
     * 错误码对应的参数
     */
    private final Object[] args;

    /**
     * 错误消息
     */
    private final String defaultMessage;

    public OkAssertException(String module, String code, Object[] args, String defaultMessage)
    {
        this.module = module;
        this.code = code;
        this.args = args;
        this.defaultMessage = defaultMessage;
    }

    public OkAssertException(String module, String code, Object[] args)
    {
        this(module, code, args, null);
    }

    public OkAssertException(String module, String defaultMessage)
    {
        this(module, null, null, defaultMessage);
    }

    public OkAssertException(String code, Object[] args)
    {
        this(null, code, args, null);
    }

    public OkAssertException(String defaultMessage)
    {
        this(null, null, null, defaultMessage);
    }

    public String getModule()
    {
        return module;
    }

    public String getCode()
    {
        return code;
    }

    public Object[] getArgs()
    {
        return args;
    }

    public String getDefaultMessage()
    {
        return defaultMessage;
    }
}
